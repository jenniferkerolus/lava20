package Class2;

public class OperatorDemo {
    public static void main(String[] args) {
        int number1=10;
        int number2=20;
        System.out.println(number1+number2);
        System.out.println(number1-number2);
        System.out.println(number1/number2);
        System.out.println(number1*number2);
        /*
        if you have to store whole number always use int
        if you have to store decimal number always use double
         */

    }
}
